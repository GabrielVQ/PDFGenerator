package PDF;

public class Energia {
    private String nombre;
    private String empresa;
    private String firma;
    private String valormedido;
    private String instrumento;
    private String proceder;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public String getFirma() {
        return firma;
    }

    public void setFirma(String firma) {
        this.firma = firma;
    }

    public String getValormedido() {
        return valormedido;
    }

    public void setValormedido(String valormedido) {
        this.valormedido = valormedido;
    }

    public String getInstrumento() {
        return instrumento;
    }

    public void setInstrumento(String instrumento) {
        this.instrumento = instrumento;
    }

    public String getProceder() {
        return proceder;
    }

    public void setProceder(String proceder) {
        this.proceder = proceder;
    }
}
