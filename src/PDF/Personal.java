package PDF;

public class Personal {
    private String nombre;
    private String empresa;
    private String fechabloqueo;
    private String horabloqueo;
    private String fechadesbloqueo;
    private String horadesbloqueo;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public String getFechabloqueo() {
        return fechabloqueo;
    }

    public void setFechabloqueo(String fechabloqueo) {
        this.fechabloqueo = fechabloqueo;
    }

    public String getHorabloqueo() {
        return horabloqueo;
    }

    public void setHorabloqueo(String horabloqueo) {
        this.horabloqueo = horabloqueo;
    }

    public String getFechadesbloqueo() {
        return fechadesbloqueo;
    }

    public void setFechadesbloqueo(String fechadesbloqueo) {
        this.fechadesbloqueo = fechadesbloqueo;
    }

    public String getHoradesbloqueo() {
        return horadesbloqueo;
    }

    public void setHoradesbloqueo(String horadesbloqueo) {
        this.horadesbloqueo = horadesbloqueo;
    }
}
